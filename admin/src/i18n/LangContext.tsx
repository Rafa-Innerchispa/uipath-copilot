type Labels = (typeof LABELS)["es"];
type PageCopy = (typeof PAGE_COPY)["es"];

const LangContext = createContext<{
  lang: Lang;
  labels: Labels;
  pages: PageCopy;
  setLang: (l: Lang) => void;
  toggleLang: () => void;
} | null>(null);

export function LangProvider({ children }: { children: ReactNode }) {
  const [lang, setLang] = useState<Lang>(() => {
    const s = localStorage.getItem("ralphi_lang");
    if (s === "en" || s === "es") return s;
    return "en";
  });
  const value = useMemo(
    () => ({
      lang,
      labels: LABELS[lang],
      pages: PAGE_COPY[lang],
      setLang: (l: Lang) => {
        localStorage.setItem("ralphi_lang", l);
        setLang(l);
      },
      toggleLang: () => {
        const next = lang === "es" ? "en" : "es";
        localStorage.setItem("ralphi_lang", next);
        setLang(next);
      },
    }),
    [lang],
  );
  return <LangContext.Provider value={value}>{children}</LangContext.Provider>;
}

export function useLang() {
  const ctx = useContext(LangContext);
  if (!ctx) throw new Error("useLang outside provider");
  return ctx;
}
